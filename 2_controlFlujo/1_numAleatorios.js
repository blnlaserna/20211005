/* NÚMEROS ALEATORIOS */
//Para programar juegos o simulaciones
//Requiere del uso del método random del objeto Math

//Obtener un número aleatorio entre cero y uno
console.log(Math.random());

//Obtener un número aleatorio entre cero y dos
console.log(Math.random()*2);

//Obtener un número decimal aleatorio entre 6 y 10
console.log(Math.random()*4 + 6);

//Calcular un número aleatorio entero entre 0 y 10 (ambos incluidos)
console.log(parseInt(Math.random()*11));

//Calcular número enteros entre 6 y 10 (ambos incluidos)
console.log(parseInt(Math.random()*5 + 6));
